const state = {
  language: "hi",
};

function applyLanguage(language) {
  const dictionary = translations[language];
  document.documentElement.lang = language;
  document.body.classList.toggle("en", language === "en");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  renderPromises(dictionary.promises);
  state.language = language;
}

function renderPromises(promises) {
  const grid = document.querySelector("[data-promises]");
  grid.innerHTML = promises
    .map(
      ([title, body], index) => `
        <article class="promise-card" data-number="${String(index + 1).padStart(2, "0")}">
          <span class="promise-icon" aria-hidden="true">
            <svg viewBox="0 0 24 24">${iconPaths[index]}</svg>
          </span>
          <h3>${title}</h3>
          <p>${body}</p>
        </article>
      `,
    )
    .join("");
}

document.querySelector("[data-language-toggle]").addEventListener("click", () => {
  applyLanguage(state.language === "hi" ? "en" : "hi");
});

document.querySelector("[data-contact-form]").addEventListener("submit", (event) => {
  event.preventDefault();
  const data = new FormData(event.currentTarget);
  const dictionary = translations.hi;
  const lines = [
    dictionary.whatsappIntro,
    `${dictionary.whatsappName}: ${data.get("name") || ""}`,
    `${dictionary.whatsappPhone}: ${data.get("phone") || ""}`,
    `${dictionary.whatsappSuggestion}: ${data.get("suggestion") || ""}`,
    `${dictionary.whatsappProblem}: ${data.get("problem") || ""}`,
  ];
  const message = encodeURIComponent(lines.join("\n"));
  window.open(`https://wa.me/918954400210?text=${message}`, "_blank", "noopener");
});

